/**
 * Test Generator
 * Generates test files for generated API routes and components
 */

import { ApiEndpointDef, PrismaModelDef } from '../../core/schema/generator';

export interface GeneratedTest {
  filename: string;
  content: string;
  type: 'unit' | 'integration' | 'e2e';
}

/**
 * Generate test files for API endpoints
 */
export function generateApiTests(
  endpoints: ApiEndpointDef[],
  models: PrismaModelDef[]
): GeneratedTest[] {
  const tests: GeneratedTest[] = [];
  
  // Group endpoints by model
  const grouped = groupByModel(endpoints);
  
  for (const [modelName, modelEndpoints] of Object.entries(grouped)) {
    const model = models.find(m => m.name === modelName);
    
    tests.push({
      filename: `__tests__/api/${modelName.toLowerCase()}.test.ts`,
      content: generateModelApiTest(modelName, modelEndpoints, model),
      type: 'integration',
    });
  }
  
  return tests;
}

function groupByModel(endpoints: ApiEndpointDef[]): Record<string, ApiEndpointDef[]> {
  const grouped: Record<string, ApiEndpointDef[]> = {};
  
  for (const endpoint of endpoints) {
    const modelName = extractModelName(endpoint.path);
    if (!grouped[modelName]) {
      grouped[modelName] = [];
    }
    grouped[modelName].push(endpoint);
  }
  
  return grouped;
}

function extractModelName(path: string): string {
  const parts = path.replace('/api/', '').split('/')[0];
  return parts
    .split('-')
    .map(p => p.charAt(0).toUpperCase() + p.slice(1))
    .join('');
}


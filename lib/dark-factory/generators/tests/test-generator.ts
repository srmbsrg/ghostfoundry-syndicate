function generateMockData(model?: PrismaModelDef): Record<string, unknown> {
  const mockData: Record<string, unknown> = {
    id: 'test-id-123',
    createdAt: new Date().toISOString(),
    updatedAt: new Date().toISOString(),
  };
  
  if (model) {
    for (const field of model.fields) {
      if (['id', 'createdAt', 'updatedAt'].includes(field.name)) continue;
      if (!isPrimitiveField(field.type)) continue;
      
      mockData[field.name] = getMockValue(field.type, field.name);
    }
  }
  
  return mockData;
}

function isPrimitiveField(type: string): boolean {
  const primitives = ['String', 'Int', 'Float', 'Boolean', 'DateTime', 'Json', 'Decimal'];
  return primitives.includes(type);
}

function getMockValue(type: string, fieldName: string): unknown {
  // Use field name hints for better mock data
  if (fieldName.toLowerCase().includes('email')) return 'test@example.com';
  if (fieldName.toLowerCase().includes('name')) return 'Test Name';
  if (fieldName.toLowerCase().includes('status')) return 'active';
  if (fieldName.toLowerCase().includes('price') || fieldName.toLowerCase().includes('amount')) return 99.99;
  if (fieldName.toLowerCase().includes('count') || fieldName.toLowerCase().includes('quantity')) return 10;
  
  // Fall back to type-based defaults
  switch (type) {
    case 'String': return 'test-string';
    case 'Int': return 42;
    case 'Float': 
    case 'Decimal': return 3.14;
    case 'Boolean': return true;
    case 'DateTime': return new Date().toISOString();
    case 'Json': return {};
    default: return null;
  }
}

function toKebabCase(str: string): string {
  return str
    .replace(/([a-z])([A-Z])/g, '$1-$2')
    .toLowerCase();
}
